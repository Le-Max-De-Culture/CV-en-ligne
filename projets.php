          <h3>Concours de vulgarisation "C'est moi le prof!"</h3>

          <p><span class="fas fa-calendar-alt"></span> 12/11/2019 - 26/01/2020</p>
          <span class="italic">Nicolas Halpern-Herla</span> et <span class="italic">Stephane Chenevière</span>, créateurs du site <a href="http://jaicompris.com/" target="_blank">jaicompris.com</a> et de la chaîne YouTube éponyme, ont organisé le concours "C'est moi le prof!" <a href="https://www.youtube.com/watch?v=zvSU2_gj9I0" target="_blank">Concours Vidéo - C'est moi le prof ! - Saison 1 - Expliquer le théorème de Pythagore + sa réciproque</a> . Ce concours consistait à réaliser une vidéo de 12 minutes maximum qui explique le théorème de Pythagore, sa réciproque et comment les utiliser en exercice. La vidéo devait être accessible à quiconque découvrant Pythagore mais aussi pour tout élève souhaitant réviser avant un contrôle sur ce théorème. Voici la vidéo que j'ai réalisé : <a href="https://www.youtube.com/watch?v=Bhz3pxa42DI" target="_blank">Le Max De Mathématique - Pythagore, son théorème, sa réciproque et sa contraposée</a>. Je voulais remercier tout ceux qui ont relu le texte de la vidéo et qui m'ont conseillé sur le montage et tout l'aspect visuel en général. Je suis arrivé deuxième ex-aequo :
              <a href="https://www.youtube.com/post/UgwX7yA92DmdiHg7ZpZ4AaABCQ" target="_blank">les résultats</a>.
            </br></br>
            Ce concours m'a permis de commencer les vidéos de vulgarisation de mathématique en complément des cours que je rédige sur le site "Le Max De Culture" (<a href="https://le-max-de-culture.fr/fr/lessons/mathematics/premiere/" target="_blank">Cours de Mathématique - Première</a>). Il a également contribué à me motiver à continuer de faire de la vulgarisation. Cela me permet en effet de comprendre en profondeur les notions abordées. C'est aussi très motivant pour apprendre et découvrir de nouvelles choses ! Je remercie donc énormément <span class="italic">Nicolas Halpern-Herla</span> et <span class="italic">Stephane Chenevière</span> pour avoir organisé ce concours qui m'a été très utile :)

