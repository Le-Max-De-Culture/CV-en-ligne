          <h3>Concours & formations de maths "olympiques"</h3>
<ul>
  <li>
    École d'été Maths-Sciences de l'École Normale Supérieure de Paris <span class="fas fa-calendar-alt"></span> 20/07/2020 - 24/07/2020
    <p> L'école d'été Maths-Sciences de l'École Normale Supérieure de Paris fut l'occasion parfaite de découvrir, notamment à travers les nombreuses conférences données par des chercheurs renommés, des mathématiques nouvelles et, plus généralement, de nouvelles notions en sciences.
            </br></br>
              Ce fut également l'occasion de rencontrer d'autres lycéens passionnés de mathématiques avec qui les conversations furent très enrichissantes. Nous avons pu, par exemple, aborder l'orientation, les métiers liés aux mathématiques, le monde de la recherche, etc. Ces derniers sujets sont souvent méconnus des lycéens et cela m'a permis de remettre en cause certaines idées reçues.
            </br></br>
              J'ai découvert cet événement grâce au partage et à la diffusion de Roger Mansuy, j'ai ensuite candidaté via la "Candidature Libre". Plus d'informations sur leur site : <a href="https://maths-sciences.ens.fr/" target="_blank">Maths-Sciences 2020</a></p>
