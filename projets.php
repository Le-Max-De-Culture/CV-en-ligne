    Coupe Animath d'automne 2020 <span class="fas fa-calendar-alt"></span> 30/09/2020 - 07/10/2020
      <p>Durant mon année de terminale (2020-2021), j'ai participé à la <a href="https://maths-olympiques.fr/?p=5612" target="_blank">Coupe Animath d'Automne 2020</a> qui consistait à résoudre 9 problèmes en 4h. Vous pouvez retrouver le sujet <a href="https://maths-olympiques.fr/wp-content/uploads/2020/10/Sujet.pdf">ici</a> et un corrigé <a href="https://maths-olympiques.fr/wp-content/uploads/2020/10/sujetcommentairecorrige.pdf">ici</a>. Les résultats sont présents <a href="https://maths-olympiques.fr/wp-content/uploads/2020/10/POFM-2020-2021.pdf">ici</a> et le palmarès <a href="https://maths-olympiques.fr/wp-content/uploads/2020/10/Coupe-Animath-2020.pdf">ici</a>. J'ai obtenu un score de 35/63 avec un "Accessit".
            </p>
  </li>
  <li>Coupe Animath de printemps 2020 <span class="fas fa-calendar-alt"></span> 03/06/2020 - 06/06/2020
    <p>Durant mon année de première (2019-2020), j'ai participé à la <a href="https://maths-olympiques.fr/?p=4681" target="_blank">Coupe Animath de printemps 2020</a> qui consistait à résoudre 9 problèmes en 4h. Vous pouvez retrouver le sujet <a href="https://maths-olympiques.fr/wp-content/uploads/2020/06/Sujet-Coupe-Animath-Printemps-2020.pdf">ici</a> et un corrigé <a href="https://maths-olympiques.fr/wp-content/uploads/2020/06/Corrig%C3%A9-Coupe-Animath-Printemps-2020.pdf">ici</a>. Les résultats sont présents <a href="https://maths-olympiques.fr/?p=4786">ici</a> et le palmarès <a href="http://igm.univ-mlv.fr/~juge/pofm/2020/coupe-animath/r%C3%A9sultats/Palmar%C3%A8s.pdf">ici</a>. J'ai obtenu un score de 38/63 avec une "Mention très honorable".
            </br></br>
            Ce fut pour moi une expérience très enrichissante sur le plan mathématique notamment. Je n'avais jamais fait de "mathématique olympique" avant, n'ayant pas pu participer aux Olympiades de mathématiques de première. En effet, je suis scolarisé dans un lycée hors contrat. Pour être tout à fait honnête, je ne pensais même pas franchir la première étape de la coupe. De ce fait, ma préparation fût très brève et ainsi, quasiment inutile.
          </br></br>
            Cette coupe m'a ainsi permis de découvrir de "nouvelles mathématiques" et m'a motivé à m'investir plus dans ces dernières par le biais notamment des cours sur <a href="https://www.mathraining.be/">Mathraining</a>.
