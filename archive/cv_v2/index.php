    <div class="section">
      <h3>L'informatique</h3>
      <p>Grâce au "Club Info'" au collège, j'ai pu être initié à la programmation de sites Web. Au collège, j'ai appris à taton le HTML, le CSS et le JavaScript mais ce n'est qu'en seconde que j'ai pris le temps de suivre une formation sur <a href="https://openclassrooms.com/">OpenClassrooms</a> à la création de sites Web. J'ai ainsi pu consolider mes connaissances en <span>HTML</span> et <span>CSS</span> et j'ai également appris à programmer en <span>PHP et MySql</span>.</p>

      <p>Pour la rédaction de documents mathématiques, j'ai aussi appris à réaliser des PDF en TeX (<a href="https://www.latex-project.org/" target="_blank">LaTeX</a>) au départ sur TexWorks, puis sur TexMaker et finalement Overleaf.</p>

      <p>Je suis également très intéressé par la sécurité informatique, notamment des sites Web. Cele m'a mené à apprendre à utiliser d'autres OS comme Kali Linux pour avoir accès à des nouveaux outils de test. J'ai ainsi peu à peu appris à utiliser Linux. Aujourd'hui, j'utiliser <span>Ubuntu et Windows en dual-boot</span>.</p>

      <p>Actuellement, je développe principalement le site de mon projet <a href="https://le-max-de-culture.fr">Le Max De Culture</a>. Vous pouvez consulter mes autres sites <a href="i">ici</a>.</p>
