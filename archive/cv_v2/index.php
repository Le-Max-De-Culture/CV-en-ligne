    <div class="section">
      <h3>La mathématique</h3>
      <p>Je suis passionné depuis plusieurs années par la Mathématique (<a href="">Découvrez pourquoi "la Mathématique"</a>). En 6e, je commençais à prendre plaisir à étudier la mathématique mais ce n'est qu'à partir de la 5e que j'ai commencé à m'épanouir grâce au dévouement de mon professeur de mathématique <a href="https://www.lateteenmaths.fr/cv_framaillard.html" target="_blank">François-Alexandre Maillard</a>.</p>

      <p>Ce n'est qu'en classe de première que je découvre la <a href="https://maths-olympiques.fr/" target="_blank">Préparation Olympique Française de Mathématiques (POFM)</a>, je participe alors à la <a href="assets/files/diplome_animath_2020.jpg" target="_blank">Coupe Animath de printemps 2020</a> où j'ai obtenu un score de 38/63 avec une "Mention très honorable". En terminale, j'ai renouvelé cette expérience très formatrice en participant à la <a href="assets/files/diplome_animath_2020_2.pdf" target="_blank">Coupe Animath d'Automne 2020</a> où j'ai obtenu un score de 35/63 avec un "Accessit".</p>

      <p>J'ai également eu la chance de participer à l'<a href="https://maths-sciences.ens.fr/" target="_blank">École d'été Maths-Sciences de l'École Normale Supérieure de Paris</a> après <a href="assets/files/AttestationLuce_ENS_2020.pdf" target="_blank">sélection</a>. Ce fut l'occasion parfaite de découvrir, notamment à travers les nombreuses conférences données par des chercheurs renommés, des mathématiques nouvelles et, plus généralement, de nouvelles notions en sciences.</p>

      <p>Je prends également beaucoup de plaisir à travailler sur des problémes mathématiques ouverts ou semi-ouverts. J'ai ainsi participé aux <a href="https://correspondances-maths.fr/" target="_blank">Correspondances de Jeunes Mathématicien·ne·s</a> en première et en terminale avec des élèves rencontrés lors du stage d'été à l'ENS.</p>
    </div>

