    <div class="section">
      <h3>Le partage</h3>
      <p>J'ai compris, avec le temps, que partager ce que l'on sait et l'enseigner était la meilleure façon d'apprendre encore plus. Ainsi, je m'engage depuis quelques années à favoriser le partage de connaissances. C'est en ce sens que j'ai créé <a href="https://le-max-de-culture.fr">Le Max De Culture</a> avec en tête, cette affirmation de Bernard Werber <span>“Le savoir n’appartient à personne, il est à la disposition de tous.”</span>.</p>

      <p>J'ai également participé en première au <a href="https://www.youtube.com/watch?v=zvSU2_gj9I0" target="_blank">Concours de vulgarisation "C'est moi le prof!"</a> organisé par Nicolas Halpern-Herla et Stephane Chenevière, créateurs du site <a href="http://jaicompris.com/" target="_blank">jaicompris.com</a> et de la chaîne YouTube éponyme où je suis arrivé deuxième ex-aequo.</p>

      <p>Je m'engage également dans la médiation scientique depuis quelques années. Je suis bénévole <a href="https://animath.fr/">Animath</a> pour l'action <a href="https://parlons-maths.fr/">Parlons Maths</a> depuis 2020. Je promeus également les événements, les conférences et toutes les actions de médiations scientifiques comme <a href="http://www.nuitdesmaths.org/edition-2020/">La Nuit Des Maths</a> auquelle j'ai pu assisté.</p>
    </div>

