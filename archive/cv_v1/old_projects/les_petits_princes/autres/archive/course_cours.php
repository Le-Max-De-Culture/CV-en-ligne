    <h1>Créer la voiture du futur !</h1>
    <p>
  Course En Cours est une compétition liant lycéens, collégiens et professeurs.
</p><p>
  Le projet consiste à créer une voiture à l’échelle 1/14ième avec un design futuriste et pensé aérodynamique afin d’être rapide. Ces voitures sont munies d’un moteur identique pour chaque équipe que les organisateurs fournissent ainsi que d’une batterie elle aussi fournie.</p><p>
  Le but est de faire un travail approfondi afin d’avoir un projet solide et le jour de la compétition remporter un maximum de points sur un ensemble de critères.</p><p>
  Pour cela chaque équipe est divisée en rôles propres à chacun, il y a donc un chef d’équipe, un ingénieur conception, un ingénieur essai, un ingénieur design et style, un responsable communication et sponsors ainsi qu’un ingénieur systèmes numériques.</p><p>
  Voici les épreuves que nous devons préparer et qui nous attendent lors de la compétition :</p><p>
<ul>
  <li class="tiret"><span>La conformité :</span> C’est l’épreuve fatale, ici un jury vérifie que notre “bolide” est aux normes, c’est à dire aux bonnes dimensions ainsi qu’à la bonne masse, il faut avoir été précis lors de la conception et des essais car cette épreuve est éliminatoire!</li>
  <li class="tiret"><span>La soutenance :</span> Il s’agit là de présenter à l’oral l’intégralité de notre projet en 8 minutes max dont une minute de présentation en anglais pour chacun, tous les membres de l’équipe doivent expliquer clairement -avec un certain degré de détail-  leur travail au sein du groupe. L’ingénieur conception à une très grande importance lors de cette épreuve!</li>
  <li class="tiret"><span>Evaluation du stand :</span> Un certains nombres de critères sont pris en compte dans cette épreuve comme l’identité d’équipe, le portfolio, l’aspect environnementale de notre travail ainsi que le détail des recettes et dépenses de l’équipe</li>
  <li class="tiret"><span>Epreuves sur piste :</span> Il s’agit là d’une épreuve complète et complexe  constituée de  3  critères et sous-épreuve qui sont :
    <ul>
      <li class="tiret">le temps de courses → doit être le plus petit possible</li>
      <li class="tiret">le temps de réaction → temps que met le “déclencheur” de la voiture a appuyer sur le bouton de démarrage après l’extinction des lumières de départ.</li>
      <li class="tiret">arrêt au stand → programmer la voiture pour qu’elle s’arrête sur la piste dans une zone définie par le jury.</li>
    </ul>
  </li>
</ul>

