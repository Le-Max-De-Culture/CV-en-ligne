<p>
  Le concours Course en Cours est un dispositif pédagogique où des
élèves, collégiens ou lycéens, en groupe de 4 à 6 membres, imaginent, conçoivent, fabriquent, testent et font concourir leur véhicule miniature lors des compétitions régionales ou nationales du concours.
​</p><p>
Dans chaque équipe, des postes sont attribués au membres des groupes : Chef d'équipe, Ingénieur de conception, Responsable Style/design, Responsable de fabrication, Responsable des essais et Responsable communication/sponsoring.
Tout le monde a un rôle aussi important les uns que les autres.
​</p><p>
Le véhicule créé doit être en rapport avec le thème, original, rapide, et design.
Pour accompagner ce véhicule lors de la présentation de celui-ci, un stand en accord avec notre thématique ainsi qu'un portfolio doivent être créé.
Tous ces équipements sont financés par les sponsors que l'équipe aura trouvé auparavant.
Pour finir, devant le jury, un oral pour présenter notre projet en détail doit être réalisé.
</p><p>
Le but principal de ce concours est de développer des vocations dans les filières scientifiques et techniques ainsi que de développer l'esprit d'équipe auprès des élèves.
</p><p>



Nous sommes une équipe de 5 :
Tout d’abord, Alexis, le chef d’équipe, il a la responsabilité de gérer le projet et son budget. Il définit les tâches, les planifie, les attribue à un responsable et s’assure régulièrement de leur bonne réalisation et de leur conformité aux spécifications.
</p><p>
Dans un deuxième temps, l'ingénieur en Fabrication, Julien
qui est en charge de la réalisation des différents
éléments constitutifs du véhicule, de son assemblage.
</p><p>
Ensuite, le responsable style-design, Lucas, il est
responsable de l’identité de l’équipe appliquée aux véhicules,
aux tenues et au stand, en veillant à obtenir une homogénéité visuelle (style, couleurs, look).
</p><p>
Le responsable communication et sponsoring est Kilian. Son objectif est de développer les outils permettant de faire connaître l’équipe ainsi que de trouver des sponsors, essentielle pour la compétition.
</p><p>
Pour finir, l’ingénieur en conception qui est responsable du développement et de la modélisation 3D du véhicule.
Par ailleurs, Alexis est également responsable des essais.
</p><p>
Pour notre thème nous nous sommes inspirés d’un jeu très célèbre qui est le monopoly.
Nous voulions
un thème originale et plus précisément un jeu de société. C’est le premier thème qui nous a convenu à tous.
Nous nous sommes inspiré du logo d’origine pour créer un logo à notre image.

